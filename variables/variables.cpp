#include <iostream>

int main() {
   int x; // declaration
   x = 10; // assignment
   int y = 5;

   std::cout << x + y << std::endl;


   double price = 10.99; // double type is for decimal number
   std::cout << price << std::endl;

   char grade = 'B'; // char type is for single character
   std::cout << grade << std::endl;

   bool student = true; // bool is for boolean, true or false value
   std::cout << student << std::endl;

   std::string name = "Nattan"; // string is for a sequence of text
   std::cout << "Hello " << name << std::endl;
   
   return 0;
}